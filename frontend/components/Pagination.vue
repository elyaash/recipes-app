<template>
    <div class="flex justify-center border-t border-gray-200 bg-white px-4 py-3">
        <nav class="isolate inline-flex -space-x-px rounded-md shadow-sm" aria-label="Pagination">
          <button
            @click="emits('handlePageClick',pagination.current_page - 1)"
            :disabled="pagination.current_page == 1"
          class="relative inline-flex items-center rounded-l-md px-2 py-2 text-gray-400 ring-1 ring-inset ring-gray-300 hover:bg-gray-50 focus:z-20 focus:outline-offset-0">
            <span class="sr-only">Previous</span>
            <svg class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor" aria-hidden="true">
              <path fill-rule="evenodd" d="M12.79 5.23a.75.75 0 01-.02 1.06L8.832 10l3.938 3.71a.75.75 0 11-1.04 1.08l-4.5-4.25a.75.75 0 010-1.08l4.5-4.25a.75.75 0 011.06.02z" clip-rule="evenodd" />
            </svg>
          </button>

          <button
            aria-current="page"
            class="btn pointer relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-900 ring-1 ring-inset ring-gray-300 hover:bg-gray-50 focus:z-20 focus:outline-offset-0"
            :class="(page == pagination.current_page )?'bg-indigo-600 hover:bg-indigo-300 text-white':''"
            v-for="page in pages()"
            :key="page"
            @click="emits('handlePageClick',page)"
             >{{ page }}</button>
           <button
           @click="emits('handlePageClick',pagination.current_page + 1)"
           :disabled="pagination.current_page == pagination.last_page"
           class="relative inline-flex items-center rounded-r-md px-2 py-2 text-gray-400 ring-1 ring-inset ring-gray-300 hover:bg-gray-50 focus:z-20 focus:outline-offset-0">
            <span class="sr-only">Next</span>
            <svg class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor" aria-hidden="true">
              <path fill-rule="evenodd" d="M7.21 14.77a.75.75 0 01.02-1.06L11.168 10 7.23 6.29a.75.75 0 111.04-1.08l4.5 4.25a.75.75 0 010 1.08l-4.5 4.25a.75.75 0 01-1.06-.02z" clip-rule="evenodd" />
            </svg>
          </button>
        </nav>
  </div>
</template>

<script setup>

const props = defineProps({
  pagination: Object
})
const emits = defineEmits(['handlePageClick'])
function pages() {
  pages=[]
  for(let i = 1; i <= props.pagination.last_page; i++) {
    pages.push(i)
  }
  return pages;
}

</script>
